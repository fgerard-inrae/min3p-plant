!c ----------------------------------------------------------------------
!c subroutine totint_w
!c --------------------
!c compute total source/sink term towards water concentration
!c due to intra-aqueous kinetic reactions or derivative thereof
!c
!c written by:      Sergi Molins - June 7, 2006
!c
!c adapted from totmin written by Uli Mayer
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c
!c passed:   idbg               = unit number - debugging file        + -
!c           totintaq_w         = total source-sink term towards      * +
!c                                water concentration due to 
!c                                intra-aqueous kinetic reactions 
!c                                or derivative thereof
!c
!c common:
!c chem.f:   real*8:
!c           -------
!c           rateaq(naq)        = reaction rates of intra-aqueous     + -
!c                                kinetic reaction
!c           xnuaq(naq*nc)      = stoichiometric coefficient of       + -
!c                                components in intra-aqueous
!c                                kinetic reaction
!c
!c           integer*4:
!c           ----------
!c           iaaq(naq+1)        = row pointer array to                + -
!c                                stoichiometric coefficients in
!c                                intra-aqueous kinetic reactions
!c           jaaq(naq*nc)       = column pointer array to             + -
!c                                stoichiometric coefficients in
!c                                intra-aqueous kinetic reactions
!c           naq                = number of intra-aqueous kinetic     + -
!c                                reactions
!c           nc                 = number of components including h2o  + -
!c
!c           character:
!c           ----------
!c           namec(nc)          = component names                     + -
!c 
!c
!c local:    real*8:
!c           -------
!c           r0                 = constant
!c
!c           integer*4:
!c           ----------
!c           i                  = counter (stoichiometric
!c                                coefficients)
!c           iaq                = counter (intra-aqueous
!c                                kinetic reactions)
!c           ic                 = counter (components) 
!c           info_debug         = flag to control debugging 
!c                                information
!c           istart             = pointer (stoichiometric 
!c                                coefficients)
!c           istop              = pointer (stoichiometric
!c                                coefficients)
!c
!c external: -
!c ----------------------------------------------------------------------
  
      subroutine totint_w(totintaq_w,tid,idbg)
 
      use chem, only : naq, iaaq, jaaq, xnuaq, rateaq, namec
      
      implicit none
      
      real*8 :: totintaq_w
      integer :: tid,idbg
      
      integer :: i, ic, iaq, istart, istop
      real*8, parameter :: r0 = 0.0d0
      
      totintaq_w = r0
       
      do iaq = 1,naq
        istart = iaaq(iaq)
        istop = iaaq(iaq+1)-1
        do i = istart,istop
          ic = jaaq(i)
 
!c  skip h2o, since not a primary unknown
 
          if (namec(ic).eq.'h2o') then
            totintaq_w = totintaq_w+xnuaq(i)*rateaq(iaq,tid)
          end if
        end do

      end do

!cdbg
!c      info_debug = 0
!
!c      if (info_debug.gt.0) then
!c        write(idbg,'(a)') 'returning from totint ...'
!c        do ic = 1,nc-1
!c          write(idbg,'(a12,1x,1pe15.6e3)') namec(ic),totintaq(ic)
!c        end do
!c        write(idbg,*) '----------------------------------------------'
!c      end if
!
!c      if (info_debug.gt.1) then
!c        stop
!c      end if
!cdbg
      return
      end
