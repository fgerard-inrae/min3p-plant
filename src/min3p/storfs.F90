!c ----------------------------------------------------------------------
!c real*8 function storfs
!c ------------------------
!c
!c compute mass storage term for fully saturated conditions
!c
!c written by:      Uli Mayer - May 6, 96
!c
!c last modified:   Uli Mayer - November 25, 96
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   hnew               = hydraulic head (new time level)     + -
!c           hold               = hydraulic head (old time level)     + -
!c           izn                = pointer (material property zone)    + -
!c           storfs             = mass storage term                   * +
!c
!c common: 
!c phys.f:   real*8:
!c           -------
!c           spstor(nzn)        = specific storage coefficient        + -
!c         
!c           integer*4:
!c           ----------
!c           nzn                = number of material property zones   + -
!c
!c local:    -
!c
!c external: -
!c ----------------------------------------------------------------------
 
      real*8 function storfs(delt,hnew,hold,izn,stor_ivol)
 
      use parm
      use phys
 
      implicit none
      
      integer :: izn
      
      real*8 :: delt,hnew, hold, stor_ivol

      storfs = stor_ivol*(hnew-hold)/delt
 
      return
      end
