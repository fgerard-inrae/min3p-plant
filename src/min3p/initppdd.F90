                end if
              end if

              call read_vtk_data_from_file(ishfp,'expn',bfound,expn_vol)
              if (bfound) then
                if (rank == 0 .and. b_enable_output) then
                  write(*,'(a)') 'read expn from vtk file: done'
                  write(ilog,'(a)') 'read expn from vtk file: done'
                end if
              end if

              call read_vtk_data_from_file(ishfp,'aentry',bfound,aentry_vol)
              if (bfound) then
                if (rank == 0 .and. b_enable_output) then
                  write(*,'(a)') 'read air entry from vtk file: done'
                  write(ilog,'(a)') 'read air entry from vtk file: done'
                end if
              end if

              !if (root_uptake) then
                call read_vtk_data_from_file(ishfp,'h1dry',bfound,h1dry_vol)
                if (bfound) then
                  if (rank == 0 .and. b_enable_output) then
                    write(*,'(a)') 'read h1dry from vtk file: done'
                    write(ilog,'(a)') 'read h1dry from vtk file: done'
                  end if
                end if
              !end if

#ifdef OPENMP
    !$omp parallel                                                    &
    !$omp if (nngl > numofloops_thred_initppvs_1)                     &
    !$omp num_threads(numofthreads_global)                            &
    !$omp default(shared)                                             &
    !$omp private (ivol)
    !$omp do schedule(static)
#endif
              do ivol = 1, nngl
!c convert VG alpha from units of 1/L to 1/pressure
                spalpha_vol(ivol) = spalpha_vol(ivol)/(gacc * ref_dens)

!c convert air entry pressure from units of L to pressure
                aentry_vol(ivol) = aentry_vol(ivol) * gacc * ref_dens

!c  assign remaining soil hydraulic function parameters
                spgamma_vol(ivol) = r1-(r1/spbeta_vol(ivol))
              end do
#ifdef OPENMP
    !$omp end do
    !$omp end parallel
#endif
            end if

            if (ishfp > 0) then
              close(ishfp)
              call lun_free(ishfp)
            end if

          end if
#endif


        end if         !variably_saturated, read soil hydraulic parameters from file



!cprovi--------------------------------------------------------------
!cprovi Read Skemton coefficient from file
!cprovi--------------------------------------------------------------      
        if (compute_ice_sheet_loading) then   
          subsection = 'read skempton coefficient from file'
         
          call findstrg(subsection,itmp,found_subsection)
        
          if (found_subsection) then
            iskempton = lun_get()

            open(iskempton,file=prefix(:l_prfx)//'.skempton',          &
                 status='unknown',form='formatted')
            skempton_field = .true.
            skempton=r1
            read(iskempton,*,err=998,end=998) cdummy
            read(iskempton,*,err=998,end=998) cdummy
            read(iskempton,*,err=998,end=998) cdummy
#ifdef USG
            if (discretization_type > 0) then
              do ivolgbl = 1,nngbl
#ifdef PETSC
                if (ibits(usg_mesh_ordering,0,2) == 3) then
                  ivol = node_idx_g2lg(node_idx_g2g_invord(ivolgbl))
                else
                  ivol = node_idx_g2lg(ivolgbl)
                end if
#else
                if (ibits(usg_mesh_ordering,0,2) == 3) then
                  ivol = node_idx_g2g_invord(ivolgbl)
                else
                  ivol = ivolgbl
                end if
#endif
                if (ivol > 0) then
                  read(iskempton,*,err=998,end=998) rdummy,rdummy,     &
                                                    rdummy,skempton(ivol)
                else
                  read(iskempton,*,err=998,end=998) rdummy
                end if
              end do
            end if
#endif
            if (discretization_type == 0) then
              nskip = 0
              do ivol = 1,nngl
#ifdef PETSC
                do iskip = 1, node_idx_lg2g(ivol) - nskip -1
                  ierrcd = 7
                  read(iskempton,*,end=999,err=999) rdummy
                end do
                nskip = node_idx_lg2g(ivol)
#endif
                read(iskempton,*,err=998,end=998)rdummy, rdummy,       &
                                                 rdummy, skempton(ivol)
              end do
            end if

            close(iskempton)
            call lun_free(iskempton)
                  
          end if

#ifdef USG
          subsection = 'read skempton coefficient from vtk file'

          call findstrg(subsection,itmp,found_subsection)

          if (found_subsection) then
            iskempton = lun_get()
            if ((read_spatial_master_proc .and. rank == 0) .or.        &
                .not.read_spatial_master_proc) then
              open(iskempton,file=prefix(:l_prfx)//'.skempton.vtk',    &
                   status='unknown',form='formatted')
            else
              call lun_free(iskempton) 
              iskempton = 0
            end if

            skempton_field = .true.
            skempton=r1

            call read_vtk_data_from_file(iskempton,'skempton',bfound,skempton)
            if (bfound) then
              if (rank == 0 .and. b_enable_output) then
                write(*,'(a)') 'read skempton from vtk file: done'
                write(ilog,'(a)') 'read skempton from vtk file: done'
              end if
            end if

            if (iskempton > 0) then
              close(iskempton)
              call lun_free(iskempton)
            end if

          end if
#endif

        end if
     
        subsection = 'residual gas saturation'
       
        call findstrg(subsection,itmp,found_subsection)
        sgr=r0
        if (found_subsection) then
          ierrcd = 8
          read(itmp,*,err=999,end=999) sgr
          if (sgr>r1.or.sgr<r0) then
            sgr=r0
          end if
        end if


!c  convert to internal time units

        if (permeability_field) then
#ifdef USG
          if (discretization_type > 0 .and. is_cell_based_perm_cond) then
#ifdef OPENMP
    !$omp parallel                                                    &
    !$omp if (num_cells > numofloops_thred_initppdd_1)                &
    !$omp num_threads(numofthreads_global)                            &
    !$omp default(shared)                                             &
    !$omp private (icell)
    !$omp do schedule(static)
