!c ----------------------------------------------------------------------
!c real*8 function fluxvs_free_drainage
!c ----------------------
!c
!c compute water flux for variably saturated conditions
!c
!c written by:      Uli Mayer - May 29, 96
!c
!c last modified:
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           ------- 
!c           relperm                =  relative permeability          + -
!c
!c
!c           perm_fac               =  permeability scaling fractor   + -
!c
!c           condz                  =  hydraulic conductivity         + -
!c
!c           grad                   =  free drainage gradient         + -
!c
!c           area                   =  boundary surface area
!c
!c           logical:
!c           --------
!c           character:
!c           ----------
!c           iups               = upstream pointer                    + -
!c
!c common:   -
!c           
!c
!c external: -
!c ----------------------------------------------------------------------
 
      real*8 function fluxvs_free_drainage(relperm,condz,perm_fac,grad,area)
 
      implicit none
      
      real*8 :: relperm,condz,perm_fac,grad,area

      
!c  compute free drainage flux

      fluxvs_free_drainage = relperm*condz*perm_fac*grad*area
 
      return
      end
