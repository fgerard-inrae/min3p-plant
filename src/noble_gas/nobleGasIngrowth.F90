        implicit none

        character(len=*), intent(in) :: strarray(:)
        character(len=*), intent(in) :: str
        integer :: idx

        !c local variables
        integer :: i

        idx = 0

        do i = 1, size(strarray)
          if (trim(strarray(i)) .eq. trim(str)) then
            idx = i
            return
          end if
        end do

      end function findloc_str_first
	  
